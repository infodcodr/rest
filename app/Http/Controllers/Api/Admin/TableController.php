    public function setposition(Request $request,$id)
    {
        try{
            $table = Table::find($id);
            $table->x = $request->x;
            $table->y = $request->y;
            $table->width = $request->width;
            $table->height = $request->height;
            $table->save();
            $data['data'] = $table;
            $data['message'] = 'update';
            return  $this->apiResponse($data,200);
        }catch(\Exception $e){
            $data['message'] = $e->getMessage();
            return  $this->apiResponse($data,404);
        }
    }
    public function getTables(Request $request,$id)
    {
        try{
            $branch = Branch::find($id);
            $data['data'] = $branch->table;
            $data['message'] = 'table List';
            return  $this->apiResponse($data,200);
        }catch(\Exception $e){
            $data['message'] = $e->getMessage();
            return  $this->apiResponse($data,404);
        }
    }
